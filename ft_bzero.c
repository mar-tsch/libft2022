#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	x;
	unsigned char	*us;

	x = 0;
	us = (unsigned char *)s;
	while (x < n)
	{
		us[x] = '\0';
		x++;
	}
}
