#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	x;

	x = 0;
	while (s1[x] != '\0' && s1[x] == s2[x] && n > 0)
	{
		x++;
		n--;
	}
	if (n == 0)
		return (0);
	return (s1[x] - s2[x]);
}
